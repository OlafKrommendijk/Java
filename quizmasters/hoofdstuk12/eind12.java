package hoofdstuk12;

public class eind12 {
	public static void main(String args[]) {
		//declareren van variabelen
		int a = 35;
		int b = 46;
		double c = 46.98;
		double d = 44.99;
		double e = 81.0;
		
		//Laat het getal dat het hoogst is zien in de uitvoer
		System.out.print(Math.max(a, b) + "\n");
		//Laat het getal dat het laagst is zien in de uitvoer
		System.out.print(Math.min(c, d) + "\n");
		//Laat de vierkantswortel zien van variabele e
		System.out.print(Math.sqrt(e) + "\n");
	}
}